def generate_ledger_pdf(
    data: dict,
    company_info: dict,
    date_from: str,
    date_to: str,
) -> bytes:
    """
    Ledger Statement PDF.
    Columns: Date | Particulars | Voucher Type | Voucher No | Debit (₹) | Credit (₹) | Balance (₹)
    Special rows: opening_row → opening style, closing_row → closing style.
    data keys: company (str), gstin (str), rows (list of dicts), opening_balance (float)
    """
    cn   = company_info.get("name", data.get("company", "Your Company"))
    addr = company_info.get("address", data.get("address", ""))
    gstin = company_info.get("gstin", data.get("gstin", ""))
    pan   = company_info.get("pan", data.get("pan", ""))

    headers = ["Date", "Particulars", "Voucher Type", "Voucher No", "Debit (₹)", "Credit (₹)", "Balance (₹)"]
    widths  = [20, 45, 28, 25, 22, 22, 25]   # mm — total ≈ 187mm ≈ 530pt
    aligns  = ["center", "left", "left", "center", "right", "right", "right"]

    all_rows: List[dict] = []

    # Opening balance row
    ob = data.get("opening_balance", 0.0)
    all_rows.append({
        "cells": ["", "Opening Balance", "", "", "", "", _indian_number(ob)],
        "row_type": "opening",
    })

    # Ledger rows
    balance = ob
    for r in data.get("rows", []):
        dr = r.get("debit", 0.0) or 0.0
        cr = r.get("credit", 0.0) or 0.0
        balance += dr - cr
        all_rows.append({
            "cells": [
                r.get("date", ""),
                r.get("particulars", r.get("party_name", "")),
                r.get("voucher_type", ""),
                r.get("voucher_no", r.get("voucher_number", "")),
                _indian_number(dr) if dr else "—",
                _indian_number(cr) if cr else "—",
                _indian_number(balance),
            ],
            "row_type": "",
        })

    # Closing balance row
    all_rows.append({
        "cells": ["", "Closing Balance", "", "", "", "", _indian_number(balance)],
        "row_type": "closing",
    })

    return pdf_build_pages(
        cn, addr, gstin, pan, "Ledger Statement", date_from, date_to,
        headers, all_rows, widths, aligns,
    )


def generate_balance_sheet_pdf(
    data: dict,
    company_info: dict,
    date_from: str,
    date_to: str,
) -> bytes:
    """
    Balance Sheet PDF.
    Two-section: Assets (numbered) then Liabilities (numbered) in same table.
    Bottom: Total Assets = Total Equity & Liabilities in bold accent.
    data keys: assets (list), liabilities (list), total_assets (float), total_liabilities (float)
    """
    cn    = company_info.get("name", "Your Company")
    addr  = company_info.get("address", "")
    gstin = company_info.get("gstin", "")
    pan   = company_info.get("pan", "")

    headers = ["#", "Ledger / Group", "Section", "Amount (₹)"]
    widths  = [10, 110, 42, 35]   # total ≈ 197mm
    aligns  = ["center", "left", "left", "right"]

    all_rows: List[dict] = []

    # Assets section
    all_rows.append({"cells": ["", "ASSETS", "", ""], "row_type": "subtotal"})
