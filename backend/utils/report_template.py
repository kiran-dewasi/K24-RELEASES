# Page setup
_W, _H = A4          # 595.28 × 841.89 pt
_LM = 20 * mm        # left margin
_RM = _W - 20 * mm   # right margin
_TM = _H - 15 * mm   # top margin
_BM = 15 * mm        # bottom margin
_CW = _RM - _LM      # content width ≈ 555 pt

# Colour palette (spec)
_C_TEXT    = colors.HexColor("#111827")
_C_ACCENT  = colors.HexColor("#4F46E5")
_C_HDR_BG  = colors.HexColor("#F3F4F6")
_C_ALT_BG  = colors.HexColor("#F9FAFB")
_C_CLOSE_BG = colors.HexColor("#E5E7EB")
_C_GRID    = colors.HexColor("#E5E7EB")
_C_MUTED   = colors.HexColor("#6B7280")
_C_HDR_BOR = colors.HexColor("#D1D5DB")
_C_OPEN_BG = colors.HexColor("#EFF6FF")
_C_SUB_BG  = colors.HexColor("#E5E7EB")   # slightly darker
_C_GREEN   = colors.HexColor("#16A34A")
_C_RED     = colors.HexColor("#DC2626")
_C_WHITE   = colors.white

# Row heights
_HDR_H = 9 * mm
_ROW_H = 7 * mm

# Rows that fit per page (approximation)
_ROWS_PER_PAGE_FIRST = 22
_ROWS_PER_PAGE_OTHER = 30


def _pdf_line(c, x1, y, x2, lw=0.4, color=_C_GRID):
    c.setStrokeColor(color)
    c.setLineWidth(lw)
    c.line(x1, y, x2, y)


def _pdf_vline(c, x, y1, y2, lw=0.4, color=_C_GRID):
    c.setStrokeColor(color)
    c.setLineWidth(lw)
    c.line(x, y1, x, y2)


def _pdf_text(c, txt, x, y, font="Helvetica", sz=9, color=_C_TEXT, align="left"):
    c.setFont(font, sz)
    c.setFillColor(color)
    s = str(txt)
    if align == "right":
        c.drawRightString(x, y, s)
    elif align == "center":
        c.drawCentredString(x, y, s)
    else:
        c.drawString(x, y, s)


def _pdf_fmt(val) -> str:
    """Format as Indian currency."""
    try:
        n = float(val)
        return _indian_number(n)
    except (TypeError, ValueError):
        return str(val) if val else "—"


def pdf_shared_header(
    c,
    company_name: str,
    company_address: str,
    gstin: str,
    pan: str,
    report_title: str,
    date_from: str,
    date_to: str,
) -> float:
    """
    Draw the shared header on the current page.
    Returns the bottom y-coordinate of the header (where content can start).

    Left:  Company name (bold 13pt) + address + GSTIN + PAN (9pt muted)
    Right: Report title (bold 14pt accent) + Period (10pt)
    Below: Horizontal divider
    """
    top = _TM

    # ── Company info (left) ──────────────────────────────────────────────────
    cy = top
    _pdf_text(c, company_name, _LM, cy, "Helvetica-Bold", 13, _C_TEXT)
    cy -= 5.5 * mm
    if company_address:
        for line in company_address.split("\n")[:2]:
            _pdf_text(c, line.strip(), _LM, cy, "Helvetica", 9, _C_MUTED)
            cy -= 4 * mm
    if gstin:
        _pdf_text(c, f"GSTIN: {gstin}", _LM, cy, "Helvetica", 9, _C_MUTED)
        cy -= 4 * mm
    if pan:
        _pdf_text(c, f"PAN: {pan}", _LM, cy, "Helvetica", 9, _C_MUTED)
        cy -= 4 * mm

    # ── Report title + period (right) ────────────────────────────────────────
    _pdf_text(c, report_title, _RM, top, "Helvetica-Bold", 14, _C_ACCENT, "right")
    period_str = f"Period: {date_from}  \u2014  {date_to}"
    _pdf_text(c, period_str, _RM, top - 5.5 * mm, "Helvetica", 10, _C_MUTED, "right")

    # ── Logo placeholder (top-right corner, 15×15mm box) ────────────────────
    # (reserved space — no image loading)

    # ── Divider ──────────────────────────────────────────────────────────────
    divider_y = min(cy, top - 5.5 * mm - 5 * mm) - 3 * mm
    _pdf_line(c, _LM, divider_y, _RM, lw=0.8, color=_C_TEXT)
    return divider_y - 3 * mm


def pdf_shared_footer(c, page: int, total_pages: int):
    """
    Draw the shared footer on the current page.
    Left: "Generated by WeAre"
    Center: "Computer generated, no signature required"
    Right: "Page X of Y"
    """
    fy = _BM + 8 * mm
    _pdf_line(c, _LM, fy, _RM, lw=0.5, color=_C_MUTED)
    _pdf_text(c, "Generated by WeAre", _LM, fy - 4.5 * mm, "Helvetica", 7.5, _C_MUTED)
    _pdf_text(c, "Computer generated, no signature required",
              _W / 2, fy - 4.5 * mm, "Helvetica", 7.5, _C_MUTED, "center")
    _pdf_text(c, f"Page {page} of {total_pages}",
              _RM, fy - 4.5 * mm, "Helvetica", 7.5, _C_MUTED, "right")


def pdf_table(
    c,
    y: float,
    headers: List[str],
    rows: List[dict],           # each dict: {"cells": [...], "row_type": ""|"opening"|"closing"|"subtotal"}
    col_widths_mm: List[float],
    col_alignments: List[str],  # "left" | "center" | "right"
) -> float:
    """
    Draw a table at position y.
    Returns the bottom y of the last row drawn.

    Row types:
      ""          → standard alternating white/#F9FAFB
      "opening"   → italic, #EFF6FF bg
      "closing"   → bold, #E5E7EB bg
      "subtotal"  → bold, slightly darker bg #D1D5DB
    """
    # Build x positions
    xs = [_LM]
    for w in col_widths_mm:
        xs.append(xs[-1] + w * mm)

    top = y
    # ── Header row ──────────────────────────────────────────────────────────
    c.setFillColor(_C_HDR_BG)
    c.rect(_LM, top - _HDR_H, _CW, _HDR_H, fill=1, stroke=0)
    _pdf_line(c, _LM, top - _HDR_H, _RM, lw=0.8, color=_C_HDR_BOR)

    for i, hdr in enumerate(headers):
        if i >= len(xs) - 1:
            break
        cell_x = xs[i]
        cell_nx = xs[i + 1]
        cell_w = cell_nx - cell_x
        align = col_alignments[i] if i < len(col_alignments) else "left"
        ty = top - _HDR_H / 2 - 2.5
        if align == "right":
            _pdf_text(c, hdr, cell_nx - 2 * mm, ty, "Helvetica-Bold", 8, _C_TEXT, "right")
        elif align == "center":
            _pdf_text(c, hdr, cell_x + cell_w / 2, ty, "Helvetica-Bold", 8, _C_TEXT, "center")
        else:
            _pdf_text(c, hdr, cell_x + 2 * mm, ty, "Helvetica-Bold", 8, _C_TEXT)

    cur_y = top - _HDR_H
    alt_counter = 0  # only counts non-special rows

    # ── Data rows ────────────────────────────────────────────────────────────
    for row_dict in rows:
        cells = row_dict.get("cells", [])
        rt = row_dict.get("row_type", "")

        # Background
        if rt == "opening":
            c.setFillColor(_C_OPEN_BG)
            c.rect(_LM, cur_y - _ROW_H, _CW, _ROW_H, fill=1, stroke=0)
        elif rt == "closing":
            c.setFillColor(_C_CLOSE_BG)
            c.rect(_LM, cur_y - _ROW_H, _CW, _ROW_H, fill=1, stroke=0)
        elif rt == "subtotal":
            c.setFillColor(colors.HexColor("#D1D5DB"))
            c.rect(_LM, cur_y - _ROW_H, _CW, _ROW_H, fill=1, stroke=0)
        else:
            if alt_counter % 2 == 1:
                c.setFillColor(_C_ALT_BG)
                c.rect(_LM, cur_y - _ROW_H, _CW, _ROW_H, fill=1, stroke=0)
            alt_counter += 1

        # Grid line
        _pdf_line(c, _LM, cur_y - _ROW_H, _RM, lw=0.3, color=_C_GRID)

        # Choose font/style per row_type
        if rt in ("closing", "subtotal"):
            fn = "Helvetica-Bold"
        elif rt == "opening":
            fn = "Helvetica-Oblique"
        else:
            fn = "Helvetica"

        for ci, cell_val in enumerate(cells):
            if ci >= len(xs) - 1:
                break
            cell_x = xs[ci]
            cell_nx = xs[ci + 1]
            cell_w = cell_nx - cell_x
            align = col_alignments[ci] if ci < len(col_alignments) else "left"
            tv = str(cell_val) if cell_val is not None else ""
            ty = cur_y - _ROW_H / 2 - 2.5

            if align == "right":
                _pdf_text(c, tv, cell_nx - 2 * mm, ty, fn, 8, _C_TEXT, "right")
            elif align == "center":
                _pdf_text(c, tv, cell_x + cell_w / 2, ty, fn, 8, _C_TEXT, "center")
            else:
                _pdf_text(c, tv, cell_x + 2 * mm, ty, fn, 8, _C_TEXT)

        cur_y -= _ROW_H

    # Outer border + column dividers
    c.setStrokeColor(_C_GRID)
    c.setLineWidth(0.5)
    c.rect(_LM, cur_y, _CW, top - cur_y, fill=0, stroke=1)
    for xi in xs[1:-1]:
        _pdf_vline(c, xi, cur_y, top, lw=0.3, color=_C_GRID)

    return cur_y


def pdf_build_pages(
    company_name: str,
    company_address: str,
    gstin: str,
    pan: str,
    report_title: str,
    date_from: str,
    date_to: str,
    headers: List[str],
    all_rows: List[dict],
    col_widths_mm: List[float],
    col_alignments: List[str],
) -> bytes:
    """
    Full PDF: header, paginated table, footer on every page.
    Returns raw PDF bytes.
    """
    # Paginate
    pages: List[List[dict]] = []
    remaining = list(all_rows)
    pages.append(remaining[:_ROWS_PER_PAGE_FIRST])
    remaining = remaining[_ROWS_PER_PAGE_FIRST:]
    while remaining:
        pages.append(remaining[:_ROWS_PER_PAGE_OTHER])
        remaining = remaining[_ROWS_PER_PAGE_OTHER:]
    if not pages:
        pages = [[]]

    total_pages = len(pages)
    buf = io.BytesIO()
    c = rl_canvas.Canvas(buf, pagesize=A4)

    for page_num, page_rows in enumerate(pages, 1):
        y = pdf_shared_header(
            c, company_name, company_address, gstin, pan,
            report_title, date_from, date_to
        )
        y = pdf_table(c, y, headers, page_rows, col_widths_mm, col_alignments)
        pdf_shared_footer(c, page_num, total_pages)
        if page_num < total_pages:
            c.showPage()

    c.save()
    buf.seek(0)
    return buf.read()


