# Style constants
_XL_HDR_FILL  = PatternFill(start_color="F3F4F6", end_color="F3F4F6", fill_type="solid")
_XL_ALT_FILL  = PatternFill(start_color="F9FAFB", end_color="F9FAFB", fill_type="solid")
_XL_TITLE_FILL = PatternFill(start_color="4F46E5", end_color="4F46E5", fill_type="solid")
_XL_CLOSE_FILL = PatternFill(start_color="E5E7EB", end_color="E5E7EB", fill_type="solid")
_XL_OPEN_FILL  = PatternFill(start_color="EFF6FF", end_color="EFF6FF", fill_type="solid")
_XL_SUB_FILL   = PatternFill(start_color="D1D5DB", end_color="D1D5DB", fill_type="solid")

_XL_FONT_BASE    = lambda bold=False: Font(name="Calibri", size=10, bold=bold)
_XL_FONT_TITLE   = Font(name="Calibri", size=12, bold=True, color="FFFFFF")
_XL_FONT_MUTED   = Font(name="Calibri", size=10, color="6B7280")
_XL_FONT_ACCENT  = Font(name="Calibri", size=12, bold=True, color="4F46E5")

_XL_ALIGN_CENTER = Alignment(horizontal="center", vertical="center", wrap_text=True)
_XL_ALIGN_LEFT   = Alignment(horizontal="left",   vertical="center", wrap_text=True)
_XL_ALIGN_RIGHT  = Alignment(horizontal="right",  vertical="center")

_XL_THIN_BORDER = Border(
    bottom=Side(style="thin", color="E5E7EB"),
    top=Side(style="thin", color="E5E7EB"),
)

_XL_NUM_FMT = '#,##0.00'          # Indian-style (openpyxl uses this directly)
_XL_NUM_FMT_IND = '[\u20b9-4009]#,##,##0.00'  # with ₹ symbol


def _xl_write_meta(ws, col_count: int, company_name: str, report_name: str, period: str):
    """
    Write the 4-row preamble:
      Row 1: Company name + report name (merged, bold 12pt accent)
      Row 2: Period + generated timestamp (merged, muted 10pt)
      Row 3: Empty
      Row 4: Column headers (written by caller)
    """
    last_col = get_column_letter(col_count)

    # Row 1 — Company + report title
    ws.merge_cells(f"A1:{last_col}1")
    cell_r1 = ws["A1"]
    cell_r1.value = f"{company_name}  |  {report_name}"
    cell_r1.font  = _XL_FONT_ACCENT
    cell_r1.fill  = PatternFill(start_color="F3F4F6", end_color="F3F4F6", fill_type="solid")
    cell_r1.alignment = _XL_ALIGN_LEFT
    ws.row_dimensions[1].height = 22

    # Row 2 — Period
    ws.merge_cells(f"A2:{last_col}2")
    cell_r2 = ws["A2"]
    cell_r2.value = f"Period: {period}    |    Generated: {datetime.now().strftime('%d-%b-%Y %I:%M %p')}"
    cell_r2.font  = _XL_FONT_MUTED
    cell_r2.alignment = _XL_ALIGN_LEFT
    ws.row_dimensions[2].height = 17

    # Row 3 — blank spacer
    ws.row_dimensions[3].height = 8

    ws.row_dimensions[4].height = 20  # header row height


def _xl_write_headers(ws, headers: List[str], alignments: List[str], row: int = 4):
    """Write the header row with #F3F4F6 fill, bold, aligned."""
    for ci, hdr in enumerate(headers, 1):
        cell = ws.cell(row=row, column=ci, value=hdr)
        cell.fill      = _XL_HDR_FILL
        cell.font      = _XL_FONT_BASE(bold=True)
        cell.alignment = _XL_ALIGN_CENTER
        cell.border    = _XL_THIN_BORDER


def _xl_write_rows(
    ws,
    data_rows: List[dict],  # {"cells": [...], "row_type": ""|"opening"|"closing"|"subtotal", "numeric_cols": set}
    alignments: List[str],
    start_row: int = 5,
):
    """Write data rows with alternating fill, number formatting, row_type styling."""
    alt_counter = 0
    for ri, row_dict in enumerate(data_rows):
        cells     = row_dict.get("cells", [])
        rt        = row_dict.get("row_type", "")
        num_cols  = row_dict.get("numeric_cols", set())  # 0-indexed set of columns that are numbers
        row_num   = start_row + ri

        # Choose fill
        if rt == "opening":
            fill = _XL_OPEN_FILL
        elif rt == "closing":
            fill = _XL_CLOSE_FILL
        elif rt == "subtotal":
            fill = _XL_SUB_FILL
        else:
            fill = _XL_ALT_FILL if (alt_counter % 2 == 1) else None
            alt_counter += 1

        bold = rt in ("closing", "subtotal")
        italic = rt == "opening"

        for ci, val in enumerate(cells, 1):
            cell = ws.cell(row=row_num, column=ci, value=val)
            if fill:
                cell.fill = fill
            cell.font = Font(name="Calibri", size=10, bold=bold, italic=italic)
            cell.border = _XL_THIN_BORDER

            col_align = alignments[ci - 1] if (ci - 1) < len(alignments) else "left"
            if col_align == "right":
                cell.alignment = _XL_ALIGN_RIGHT
                if isinstance(val, (int, float)):
                    cell.number_format = _XL_NUM_FMT
            elif col_align == "center":
                cell.alignment = _XL_ALIGN_CENTER
            else:
                cell.alignment = _XL_ALIGN_LEFT

        ws.row_dimensions[row_num].height = 16


def _xl_auto_width(ws, headers: List[str], data_rows: List[dict]):
    """Auto-fit column widths based on content."""
    col_widths = [max(len(str(h)), 8) for h in headers]
    for row_dict in data_rows:
        for ci, val in enumerate(row_dict.get("cells", [])):
            if ci < len(col_widths):
                col_widths[ci] = max(col_widths[ci], min(len(str(val or "")), 40))
    for ci, width in enumerate(col_widths, 1):
        ws.column_dimensions[get_column_letter(ci)].width = width + 3


def excel_build_workbook(
    company_name: str,
    report_name: str,
    period: str,
    headers: List[str],
    data_rows: List[dict],    # list of {"cells": [...], "row_type": "", "numeric_cols": set}
    col_alignments: List[str],
    sheet_name: str = "Report",
) -> bytes:
    """
    Build a styled XLSX workbook and return raw bytes.
    Sheet structure:
      Row 1: Company + report title (merged, accent)
      Row 2: Period (merged, muted)
      Row 3: empty
      Row 4: headers (#F3F4F6, bold)
      Row 5+: data
    Top data row (row 4 headers) is frozen.
    """
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = sheet_name[:31]

    col_count = max(len(headers), 1)
    _xl_write_meta(ws, col_count, company_name, report_name, period)
    _xl_write_headers(ws, headers, col_alignments, row=4)
    _xl_write_rows(ws, data_rows, col_alignments, start_row=5)
    _xl_auto_width(ws, headers, data_rows)

    # Freeze row 4 (freeze below header at row 5)
    ws.freeze_panes = "A5"

    buf = io.BytesIO()
    wb.save(buf)
    buf.seek(0)
    return buf.read()


