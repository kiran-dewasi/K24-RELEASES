def generate_ledger_excel(
    data: dict,
    company_info: dict,
    date_from: str,
    date_to: str,
) -> bytes:
    """Ledger Statement Excel."""
    cn   = company_info.get("name", data.get("company", "Your Company"))
    period = f"{date_from} — {date_to}"
    headers = ["Date", "Particulars", "Voucher Type", "Voucher No", "Debit (₹)", "Credit (₹)", "Balance (₹)"]
    aligns  = ["center", "left", "left", "center", "right", "right", "right"]

    rows: List[dict] = []
    ob = data.get("opening_balance", 0.0)
    rows.append({"cells": ["", "Opening Balance", "", "", "", "", ob], "row_type": "opening"})

    balance = ob
    for r in data.get("rows", []):
        dr = r.get("debit", 0.0) or 0.0
        cr = r.get("credit", 0.0) or 0.0
        balance += dr - cr
        rows.append({
            "cells": [
                r.get("date", ""),
                r.get("particulars", r.get("party_name", "")),
                r.get("voucher_type", ""),
                r.get("voucher_no", r.get("voucher_number", "")),
                dr if dr else None,
                cr if cr else None,
                balance,
            ],
            "row_type": "",
        })

    rows.append({"cells": ["", "Closing Balance", "", "", "", "", balance], "row_type": "closing"})
    return excel_build_workbook(cn, "Ledger Statement", period, headers, rows, aligns, "Ledger")


