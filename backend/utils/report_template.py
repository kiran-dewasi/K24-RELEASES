def _indian_number(val) -> str:
    """Format a number in Indian system: 1,23,456.78"""
    try:
        n = float(val)
    except (TypeError, ValueError):
        return str(val) if val else "—"
    neg = n < 0
    n = abs(n)
    int_part = int(n)
    frac = f"{n - int_part:.2f}"[1:]  # ".XX"
    s = str(int_part)
    if len(s) <= 3:
        formatted = s
    else:
        last3 = s[-3:]
        rest = s[:-3]
        groups = []
        while len(rest) > 2:
            groups.append(rest[-2:])
            rest = rest[:-2]
        if rest:
            groups.append(rest)
        groups.reverse()
        formatted = ",".join(groups) + "," + last3
    return ("-" if neg else "") + formatted + frac


