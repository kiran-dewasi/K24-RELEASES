"""
backend/utils/report_template.py

Shared base template for K24 PDF and Excel report generation.
No FastAPI / database imports — pure reportlab + openpyxl.

Design spec:
  PDF:
    Page:    A4 portrait, 20mm margins
    Font:    Helvetica (built-in, no custom loading)
    Colors:  Text #111827 | Accent #4F46E5 | Header bg #F3F4F6
             Alt row #F9FAFB | Closing row #E5E7EB | Grid #E5E7EB
    Header:  Company left, report title right — repeats every page
    Footer:  Generated by WeAre | Computer generated | Page X of Y

  Excel:
    Font:    Calibri 10pt
    Header:  #F3F4F6 fill, bold, centered
    Alt:     white / #F9FAFB
    Numbers: Indian accounting format
    Freeze:  top data row (row 4)
    Meta:    Row 1 company+report, Row 2 period, Row 3 empty, Row 4 headers

All public functions return bytes.
"""
